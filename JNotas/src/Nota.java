import java.util.Date;
import java.io.FileWriter;
import java.io.IOException;

public class Nota extends BussinessObject {
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// ATRIBUTOS
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################

	private int idCarpeta;
	private String titulo;
	private String descripcion;
	private Date fechaCreacion;
	private Date fechaInicio;
	private Date fechaFin;
	private int prioridad;

	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// GETTERS/SETTERS
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	public int getIdCarpeta() {
		return idCarpeta;
	}
	
	public void setIdCarpeta(int idCarpeta) {
		this.idCarpeta = idCarpeta;
	}

	public String getTitulo() {
		return titulo;
	}
	
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	public String getDescripcion() {
		return descripcion;
	}
	
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	public Date getFechaCreacion() {
		return fechaCreacion;
	}
		
	public Date getFechaInicio() {
		return fechaInicio;
	}
	
	public void setFechaInicio(Date fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	
	public Date getFechaFin() {
		return fechaFin;
	}
	
	public void setFechaFin(Date fechaFin) {
		this.fechaFin = fechaFin;
	}	

	public int getPrioridad() {
		return prioridad;
	}

	public void setPrioridad(int prioridad) {
		this.prioridad = prioridad;
	}
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// CONSTRUCTOR
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	public Nota() {
		super();
		this.id = -1;
		this.idCarpeta = -1;
		this.fechaCreacion = new Date();
		this.titulo = new String("Nueva nota (" + String.valueOf(this.id) + ")");
		this.descripcion = new String("");
	}
	public Nota(int idCarpeta) {
		this();
		this.idCarpeta = idCarpeta;
	}
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// SOBRECARGA DE OBJECT
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	public String toString() {
		return 
				"[p." + String.valueOf(this.prioridad) + "] " +
				this.titulo + " (id." + String.valueOf(this.id) + ")";  
	
	}
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// VALIDACIÃ“N
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	// ACCESO A DATOS
	// ##################################################################################
	// ##################################################################################
	// ##################################################################################
	
	public void Save() throws IOException {
		FileWriter f = new FileWriter("Notas.txt", true);
		f.write(String.valueOf(this.id) + ";");
		f.write(String.valueOf(this.idCarpeta) + ";");
		f.write(this.titulo + ";");
		f.write(this.descripcion + ";");
		f.write(String.valueOf(this.prioridad) + ";");
		f.write("\n");
		
		f.close();
	}
	// ##################################################################################
}
